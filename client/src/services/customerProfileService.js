export const getCLUSIT = (country) => {
  var riskOfArea = 0;

  switch (country) {
    case 'CN':
      riskOfArea = 1;
      break;
    case 'US':
      riskOfArea = 0.9;
      break;
    case 'TR':
      riskOfArea = 0.8;
      break;
    case 'RU':
      riskOfArea = 0.7;
      break;
    case 'TW':
      riskOfArea = 0.6;
      break;
    case 'BR':
      riskOfArea = 0.5;
      break;
    case 'RO':
      riskOfArea = 0.4;
      break;
    case 'IN':
      riskOfArea = 0.3;
      break;
    case 'IT':
      riskOfArea = 0.2;
      break;
    case 'HU':
      riskOfArea = 0.1;
      break;
    default:
      riskOfArea = 0;
  }

  const continent = getContinentFromId(country);

  switch (continent) {
    case 'AF':
      riskOfArea = riskOfArea + 1;
      break;
    case 'OC':
      riskOfArea = riskOfArea + 1.5;
      break;
    case 'AS':
      riskOfArea = riskOfArea + 2;
      break;
    case 'EU':
      riskOfArea = riskOfArea + 2;
      break;
    case 'NA':
      riskOfArea = riskOfArea + 4;
      break;
    case 'SA':
      riskOfArea = riskOfArea + 4;
      break;
    default:
      riskOfArea = riskOfArea;
  }

  return riskOfArea;
};

